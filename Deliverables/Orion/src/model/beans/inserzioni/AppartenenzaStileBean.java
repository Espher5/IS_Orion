package model.beans.inserzioni;

public class AppartenenzaStileBean {
	private long idInserzione;
	private String nomeStile;
	
	public long getIdInserzione() {
		return idInserzione;
	}
	public void setIdInserzione(long idInserzione) {
		this.idInserzione = idInserzione;
	}
	public String getNomeStile() {
		return nomeStile;
	}
	public void setNomeStile(String nomeStile) {
		this.nomeStile = nomeStile;
	}
}